class Car{
	private String make;
	private String model;
	private int year;
	public void setMake(String n) {
		this.make=n;
	}
	public void setModel(String m) {
		this.model=m;
	}
	public void setYear(int y) {
		this.year=y;
	}
	public String getMake() {
		return this.make;
	}
	public String getModel() {
		return this.model;
	}
	public int getYear() {
		return this.year;
	}
}